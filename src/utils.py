import logging
from fpdf import FPDF
from pathlib import Path
from typing import Dict, Any
import sys

# Add project root to path
sys.path.append(str(Path(__file__).resolve().parent.parent))
import config

logger = logging.getLogger(__name__)

class PredictionPDF(FPDF):
    """
    Custom PDF document generator for prediction certificates.
    Inherits from FPDF to provide standard clean headers and footers.
    """
    
    def header(self):
        # Draw top accent bar (slate gray #374151)
        self.set_fill_color(55, 65, 81)
        self.rect(0, 0, 210, 10, "F")
        
        # Logo or Title
        self.set_font("Helvetica", "B", 14)
        self.set_text_color(55, 65, 81)
        self.cell(0, 15, "House Price Valuation Report", ln=True, align="L")
        
        # Draw subtle border line under header
        self.set_draw_color(229, 231, 235)
        self.line(10, 28, 200, 28)
        self.ln(10)

    def footer(self):
        # Position at 1.5 cm from bottom
        self.set_y(-15)
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(156, 163, 175)
        # Page number
        self.cell(0, 10, f"Page {self.page_no()} | Generated by House Price Predictor SaaS", align="C")

def generate_prediction_pdf(prediction_data: Dict[str, Any], output_path: Path) -> Path:
    """
    Generates a premium minimalist PDF containing the details of a single prediction.
    """
    logger.info(f"Generating valuation PDF -> {output_path}")
    
    try:
        pdf = PredictionPDF(orientation="P", unit="mm", format="A4")
        pdf.set_margins(20, 20, 20)
        pdf.add_page()
        pdf.set_auto_page_break(auto=True, margin=15)
        
        # --- Section 1: Valuation Summary ---
        pdf.set_font("Helvetica", "B", 18)
        pdf.set_text_color(17, 24, 39) # Charcoal
        pdf.cell(0, 10, "VALUATION SUMMARY", ln=True)
        pdf.ln(3)
        
        pdf.set_font("Helvetica", "", 10)
        pdf.set_text_color(107, 114, 128) # Muted gray
        pdf.cell(0, 5, f"Date: {prediction_data.get('timestamp')}", ln=True)
        pdf.cell(0, 5, f"Report ID: VAL-{prediction_data.get('timestamp', '').replace('-', '').replace(':', '').replace(' ', '-')}", ln=True)
        pdf.ln(8)
        
        # Price Panel (Callout Card)
        # Light gray background container for the estimated price
        pdf.set_fill_color(249, 250, 251) # Off-white
        pdf.set_draw_color(243, 244, 246)
        pdf.rect(20, 52, 170, 30, "DF")
        
        # Print Price inside container
        pdf.set_xy(25, 57)
        pdf.set_font("Helvetica", "B", 11)
        pdf.set_text_color(107, 114, 128)
        pdf.cell(0, 5, "ESTIMATED MARKET VALUE", ln=True)
        
        pdf.set_xy(25, 64)
        pdf.set_font("Helvetica", "B", 24)
        pdf.set_text_color(79, 70, 229) # Premium Indigo
        formatted_price = f"{config.CURRENCY_SYMBOL}{prediction_data.get('estimated_price', 0.0):,.0f}"
        pdf.cell(0, 12, formatted_price, ln=True)
        
        # Reset position after absolute positioning of callout card
        pdf.set_xy(20, 90)
        
        # --- Section 2: Property Specifications ---
        pdf.set_font("Helvetica", "B", 14)
        pdf.set_text_color(17, 24, 39)
        pdf.cell(0, 10, "Property Specifications", ln=True)
        pdf.ln(2)
        
        # Table of inputs
        # Headers
        pdf.set_fill_color(243, 244, 246)
        pdf.set_font("Helvetica", "B", 10)
        pdf.set_text_color(55, 65, 81)
        
        pdf.cell(85, 8, "Feature Description", border=1, fill=True, align="L")
        pdf.cell(85, 8, "Input Value", border=1, fill=True, align="R")
        pdf.ln()
        
        # Data rows
        pdf.set_font("Helvetica", "", 10)
        pdf.set_text_color(75, 85, 99)
        
        # Format values
        inputs = [
            ("Square Footage (Living Area)", f"{prediction_data.get('sqft', 0.0):,.1f} sq ft"),
            ("Bedrooms count", f"{int(prediction_data.get('bedrooms', 0))}"),
            ("Bathrooms count (including half-baths)", f"{float(prediction_data.get('bathrooms', 0.0)):g}")
        ]
        
        for feature, val in inputs:
            pdf.cell(85, 8, f"  {feature}", border=1, align="L")
            pdf.cell(85, 8, f"{val}  ", border=1, align="R")
            pdf.ln()
            
        pdf.ln(8)
        
        # --- Section 3: Model Context & Disclaimers ---
        pdf.set_font("Helvetica", "B", 12)
        pdf.set_text_color(17, 24, 39)
        pdf.cell(0, 10, "Model Insights & Valuation Notes", ln=True)
        pdf.ln(2)
        
        pdf.set_font("Helvetica", "", 9)
        pdf.set_text_color(107, 114, 128)
        
        confidence_msg = prediction_data.get("confidence_message", "")
        pdf.multi_cell(0, 5, f"Validation Status: {confidence_msg}")
        pdf.ln(4)
        
        disclaimer = (
            "Disclaimer: This valuation estimate is generated by a Linear Regression Machine Learning model trained "
            "on historical property datasets. It is meant for educational and informational purposes only. It does "
            "not constitute a professional appraisal, financial advice, or an offer to buy or sell property. Real estate "
            "valuations fluctuate based on market factors, specific location details, property conditions, and external "
            "economic conditions not captured in this basic model."
        )
        pdf.multi_cell(0, 4.5, disclaimer)
        
        # Save PDF to disk
        pdf.output(str(output_path))
        logger.info("PDF valuation certificate generated successfully.")
        
    except Exception as e:
        logger.error(f"Error generating PDF certificate: {str(e)}")
        raise e
        
    return output_path
